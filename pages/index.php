            <h1 style="color:whitesmoke;;">Biographie</h1>
            <div class="para">
                <p> Débutant à l'orgue Hammond et au piano dès l'âge de 2 ans1, Cory Henry débute sur scène à l'âge de 6 ans en devenant finaliste du concours amateur de l'Apollo Theater d'Harlem. Peu après ses vingt ans, il devient sideman, notamment pour Kenny Garrett, le groupe de hip-hop américain The Roots ou encore Bruce Springsteen.</p>
                <p>Depuis 2012, il est membre du groupe Snarky Puppy, collectif jazz-funk basé à Brooklyn avec lequel il remporte le Grammy Award 2014 de la meilleure prestation R&B et celui du meilleur album instrumental contemporain en 2015.</p>
                <p> Son cinquième enregistrement The Revival, enregistré lors d'un concert new-yorkais en septembre 2014, sort en 2016 chez Decca Records. Cory Henry est seulement accompagné d'un batteur sur ce disque mêlant gospels et reprises de classiques pop et jazz.

                        En mars 2017, il accompagne en tournée le groupe ManDoki Soulmates le temps de 3 dates européennes à Paris, Londres et Berlin.

                        Le 13 juillet 2018, il sort l'EP Art of Love avec son groupe The Funk Apostles4. L'enregistrement en live des chansons de cet album le 24 septembre 2018 au Resident DTLA donne lieu à la sortie de Art of Love (Live in LA) le 28 août 2020.

                        Le 30 octobre 2020, il sort l'album studio Something to Say dont 4 titres ont été enregistrés avec son groupe The Funk Apostles.</p>
                        <p>    Le multi-instrumentiste Cory Henry revisite les classiques de la soul et du funk avec une énergie folle. Indiscutable star du moment, reconnu pour la richesse harmonique de son jeu d’orgue Hammond, il capture dans sa musique l’essence même du jazz et du gospel. Il produit, à partir de ces deux composantes, un nouveau langage.

                                Tireur d’élite aux côtés de Kenny Garrett, des légendaires The Roots ou même de Bruce Springsteen, Cory Henry est un maître incontesté  du groove dans le big band phénomène de Brooklyn, Snarky Puppy.
                                Il est aussi un showman exceptionnel possédant l’imprévisibilité virtuose des plus grands… Désormais à la tête des bien nommés Funk Apostles, le musicien s’approprie les classiques de la soul et du funk et enchaîne les riffs jusqu’à ce que vos pieds réclament une trêve. Du jamais vu depuis Maceo Parker.</p>
